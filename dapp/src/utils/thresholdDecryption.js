/**
 * Threshold Decryption Utility – Phase 4
 *
 * Implements:
 *  - Lagrange interpolation over integers (Shamir secret reconstruction)
 *  - Paillier homomorphic decryption using the reconstructed lambda (λ)
 *
 * All arithmetic is done with BigInt to handle 2048-bit numbers correctly.
 */

// ---------------------------------------------------------------------------
// BigInt helpers
// ---------------------------------------------------------------------------

/**
 * Fast modular exponentiation: (base ^ exp) mod modulus
 */
function modPow(base, exp, modulus) {
  if (modulus === 1n) return 0n;
  let result = 1n;
  base = ((base % modulus) + modulus) % modulus;
  while (exp > 0n) {
    if (exp & 1n) result = (result * base) % modulus;
    exp >>= 1n;
    base = (base * base) % modulus;
  }
  return result;
}

/**
 * Extended Euclidean Algorithm – returns { gcd, x, y } such that a*x + b*y = gcd
 */
function extendedGcd(a, b) {
  if (b === 0n) return { gcd: a, x: 1n, y: 0n };
  const { gcd, x, y } = extendedGcd(b, a % b);
  return { gcd, x: y, y: x - (a / b) * y };
}

/**
 * Modular multiplicative inverse: a^-1 mod m
 * Throws if inverse does not exist.
 */
function modInverse(a, m) {
  const { gcd, x } = extendedGcd(((a % m) + m) % m, m);
  if (gcd !== 1n) throw new Error('Modular inverse does not exist');
  return ((x % m) + m) % m;
}

// ---------------------------------------------------------------------------
// Shamir Secret Reconstruction (Lagrange interpolation at x = 0)
// ---------------------------------------------------------------------------

/**
 * Reconstruct the Shamir secret from a threshold number of shares.
 *
 * Each share is an object with numeric `x`, string `y`, and optionally
 * string `prime` fields (as stored in the trustee_shares/*.json files after
 * being decrypted by decryptShareY).
 *
 * When `prime` is present (generated by the fixed backend), uses modular
 * Lagrange interpolation over GF(prime) — information-theoretically secure.
 * Falls back to rational interpolation for legacy shares without a prime.
 *
 * @param {Array<{x: number, y: string, prime?: string}>} shares
 * @returns {BigInt} reconstructed secret (lambda)
 */
export function reconstructSecret(shares) {
  if (shares.length < 2) throw new Error('Need at least 2 shares to reconstruct');

  // Convert to BigInt pairs
  const pts = shares.map(s => ({ x: BigInt(s.x), y: BigInt(s.y) }));

  // --- Modular path (secure, preferred) ---
  const primeStr = shares[0].prime;
  if (primeStr) {
    const p = BigInt(primeStr);
    let secret = 0n;

    for (let i = 0; i < pts.length; i++) {
      let num = 1n;
      let den = 1n;

      for (let j = 0; j < pts.length; j++) {
        if (i === j) continue;
        num = (num * ((0n - pts[j].x + p) % p)) % p;
        den = (den * ((pts[i].x - pts[j].x + p) % p)) % p;
      }

      const lagrange = (num * modInverse(den, p)) % p;
      secret = (secret + pts[i].y * lagrange) % p;
    }

    return secret;
  }

  // --- Legacy rational path (kept for backward compatibility) ---
  // Lagrange interpolation at x = 0 over rationals, accumulated as fractions
  // f(0) = Σ_i  y_i * Π_{j≠i} (0 - x_j) / (x_i - x_j)
  let numeratorAcc = 0n;
  let denominatorAcc = 1n;

  for (let i = 0; i < pts.length; i++) {
    let num = pts[i].y;   // y_i
    let den = 1n;

    for (let j = 0; j < pts.length; j++) {
      if (i === j) continue;
      num *= (0n - pts[j].x);   // × (-x_j)
      den *= (pts[i].x - pts[j].x); // × (x_i - x_j)
    }

    // Accumulate: acc = acc + num/den
    // Using cross-multiplication: (a/b) + (c/d) = (a*d + c*b) / (b*d)
    numeratorAcc = numeratorAcc * den + num * denominatorAcc;
    denominatorAcc = denominatorAcc * den;
  }

  // The secret is the integer result of numeratorAcc / denominatorAcc
  if (denominatorAcc === 0n) throw new Error('Degenerate shares: denominator is zero');
  if (numeratorAcc % denominatorAcc !== 0n) {
    throw new Error('Share reconstruction yielded non-integer result – shares may be corrupt');
  }

  return numeratorAcc / denominatorAcc;
}

// ---------------------------------------------------------------------------
// Paillier Decryption
// ---------------------------------------------------------------------------

/**
 * Paillier L-function: L(u) = (u - 1) / n
 */
function L(u, n) {
  return (u - 1n) / n;
}

/**
 * Decrypt a Paillier ciphertext using the reconstructed lambda (λ).
 *
 * Algorithm (matching tally_coordinator.py _decrypt_with_lambda):
 *   u   = c^λ  mod n²
 *   L_u = (u - 1) / n
 *   μ   = λ^{-1}  mod n
 *   m   = L_u * μ  mod n
 *
 * @param {string} ciphertext  – encrypted tally as decimal string
 * @param {BigInt}  lambda     – reconstructed private key component
 * @param {string}  publicKeyN – Paillier modulus N as decimal string
 * @returns {BigInt} decrypted plaintext
 */
export function paillierDecrypt(ciphertext, lambda, publicKeyN) {
  const c   = BigInt(ciphertext);
  const n   = BigInt(publicKeyN);
  const n2  = n * n;
  const lam = lambda < 0n ? ((lambda % n) + n) % n : lambda;

  const u   = modPow(c, lam, n2);
  const L_u = L(u, n);
  const mu  = modInverse(lam, n);
  const m   = (L_u * mu) % n;

  return m;
}

// ---------------------------------------------------------------------------
// High-level: full Phase 4 decryption
// ---------------------------------------------------------------------------

/**
 * Run the full threshold decryption pipeline.
 *
 * Security note: λ is reconstructed locally, used only for decryption, and
 * never leaves this function (not returned, not logged, not persisted).
 *
 * @param {string}                encryptedTally – from blockchain (decimal string)
 * @param {Array<{x,y,prime}>}    shares         – decrypted trustee share objects
 * @param {string}                publicKeyN     – Paillier N from blockchain
 * @returns {{ plaintext: string }}
 */
export function thresholdDecrypt(encryptedTally, shares, publicKeyN) {
  if (!encryptedTally || !publicKeyN) throw new Error('Missing encryptedTally or publicKeyN');
  if (!shares || shares.length === 0) throw new Error('No trustee shares provided');

  // Reconstruct λ – stays scoped to this function, never returned or exposed
  const lambda = reconstructSecret(shares);

  // Decrypt
  const plaintext = paillierDecrypt(encryptedTally, lambda, publicKeyN);

  // Return only the plaintext vote count – λ goes out of scope here
  return { plaintext: plaintext.toString() };
}

