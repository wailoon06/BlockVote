/**
 * Extract per-candidate vote counts from the decrypted aggregate sum (msum).
 *
 * Encoding at vote time:  plaintext = B ^ candidateIndex
 * After aggregation:      msum = sum of all plaintexts
 * Extraction:             count[i] = floor(msum / B^i) mod B
 *
 * @param {string|BigInt} msum          - Decrypted aggregate as decimal string or BigInt
 * @param {number}        numCandidates - Number of candidate slots
 * @param {string|BigInt} voteBlock     - Slot base B used at encryption time
 * @returns {Object} Map of candidateIndex (string) → vote count (number)
 */
export function extractVoteCounts(msum, numCandidates, voteBlock) {
  let tmp = BigInt(msum);
  const B = BigInt(voteBlock);
  const perCandidateVotes = {};
  for (let i = 0; i < numCandidates; i++) {
    perCandidateVotes[String(i)] = Number(tmp % B);
    tmp /= B;
  }
  return perCandidateVotes;
}

// ---------------------------------------------------------------------------
// AES-256-GCM share decryption (browser — Web Crypto API)
// Mirrors the Node.js encryptShareY() in smart-contract/shareEncryption.js
// ---------------------------------------------------------------------------

/**
 * Convert a lowercase hex string to a Uint8Array.
 * @param {string} hex
 * @returns {Uint8Array}
 */
function hexToBytes(hex) {
  const bytes = new Uint8Array(hex.length / 2);
  for (let i = 0; i < bytes.length; i++) {
    bytes[i] = parseInt(hex.slice(i * 2, i * 2 + 2), 16);
  }
  return bytes;
}

/**
 * Decrypt a trustee share's `encrypted_y` payload in the browser using the Web Crypto API.
 *
 * Parameters must match the Node.js side:
 *   PBKDF2-SHA256, 100 000 iterations → AES-256-GCM, 128-bit tag
 *
 * @param {{ salt: string, nonce: string, ciphertext: string, tag: string }} encryptedY
 *        Hex-encoded fields from the trustee share JSON file.
 * @param {string} passphrase  Trustee passphrase (UTF-8)
 * @returns {Promise<string>}  Plaintext y value (large decimal integer string)
 * @throws {Error}  If passphrase is wrong (auth-tag mismatch) or data is corrupted.
 */
export async function decryptShareY(encryptedY, passphrase) {
  const enc = new TextEncoder();
  const dec = new TextDecoder();

  // 1. Import raw passphrase bytes as PBKDF2 key material
  const keyMaterial = await crypto.subtle.importKey(
    'raw',
    enc.encode(passphrase),
    { name: 'PBKDF2' },
    false,
    ['deriveBits']
  );

  // 2. Derive 256-bit AES key (same params as Node.js pbkdf2Sync)
  const salt    = hexToBytes(encryptedY.salt);
  const keyBits = await crypto.subtle.deriveBits(
    { name: 'PBKDF2', salt, iterations: 100_000, hash: 'SHA-256' },
    keyMaterial,
    256
  );

  // 3. Import derived key as AES-GCM key
  const aesKey = await crypto.subtle.importKey(
    'raw',
    keyBits,
    { name: 'AES-GCM' },
    false,
    ['decrypt']
  );

  // 4. Web Crypto AES-GCM expects ciphertext || tag concatenated
  const ciphertext       = hexToBytes(encryptedY.ciphertext);
  const tag              = hexToBytes(encryptedY.tag);
  const ciphertextWithTag = new Uint8Array(ciphertext.length + tag.length);
  ciphertextWithTag.set(ciphertext);
  ciphertextWithTag.set(tag, ciphertext.length);

  const nonce = hexToBytes(encryptedY.nonce);

  try {
    const plaintextBuf = await crypto.subtle.decrypt(
      { name: 'AES-GCM', iv: nonce, tagLength: 128 },
      aesKey,
      ciphertextWithTag
    );
    return dec.decode(plaintextBuf);
  } catch {
    throw new Error('Incorrect passphrase or corrupted share file — AES-GCM authentication failed');
  }
}

export default { reconstructSecret, paillierDecrypt, thresholdDecrypt, extractVoteCounts, decryptShareY };
